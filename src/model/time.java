package model;

import java.sql.Date;

/**
 *
 * @author huulo
 */
public class time {
    private int id;
    private java.sql.Date created_at;
    private java.sql.Date updated_at;
    private java.sql.Date deleted_at;
    private int project_id;
    private int task_id;
    private int account_id;
    private double hours;
    private java.sql.Date date;
    private double hour_with_timer;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getCreated_at() {
        return created_at;
    }

    public void setCreated_at(Date created_at) {
        this.created_at = created_at;
    }

    public Date getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(Date updated_at) {
        this.updated_at = updated_at;
    }

    public Date getDeleted_at() {
        return deleted_at;
    }

    public void setDeleted_at(Date deleted_at) {
        this.deleted_at = deleted_at;
    }

    public int getProject_id() {
        return project_id;
    }

    public void setProject_id(int project_id) {
        this.project_id = project_id;
    }

    public int getTask_id() {
        return task_id;
    }

    public void setTask_id(int task_id) {
        this.task_id = task_id;
    }

    public int getAccount_id() {
        return account_id;
    }

    public void setAccount_id(int account_id) {
        this.account_id = account_id;
    }

    public double getHours() {
        return hours;
    }

    public void setHours(double hours) {
        this.hours = hours;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public double getHour_with_timer() {
        return hour_with_timer;
    }

    public void setHour_with_timer(double hour_with_timer) {
        this.hour_with_timer = hour_with_timer;
    }
    
    
}
